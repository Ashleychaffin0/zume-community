        <h3>A Prayer of Saturation</h3>
        <p>
            "Our Father in heaven, you alone deserve to be worshipped in every place in the world by every
            language, culture, and group of people. That You are not ... is <span style="text-decoration: underline;">the greatest injustice of our
                generation</span>. It is the injustice that causes all other injustices, evils, and sorrows around
            us.
        </p>
        <p>
            Father, give us <span style="text-decoration: underline;">thankful eyes</span> to see where your kingdom is coming and will is
            being done on earth as it is in heaven.
        </p>
        <p>
            Father, also, give us <span style="text-decoration: underline;">burdened eyes</span> to see where your kingdom isn't. Show us those impoverished places
            where happiness in You, joy in You, life in You, and worship of You has not come yet.
        </p>
        <p>
            And, Father, give us <span style="text-decoration: underline;">the heart of Jesus</span> to go there at whatever cost to declare the kingdom,
            bring blessing, and reconcile that place to you.
        </p>
